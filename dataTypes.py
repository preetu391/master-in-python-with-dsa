#numeric data type

num1 = 10 #int
num2 = 1.23 #float
num3 = 2+3j #complex

print(type(num1))

#lists and tuples

sequence1 = [1,2,3,4] #list and it is mutable
sequence2 = (2,3,4) #tuple and it is immutable

print(type(sequence2))

#dictionary (key:value)

dict1 = {"Class": "Students", "Company": "Employees"}

print(type(dict1))

#boolean

boolean = True
boolean2 = False

print(type(boolean))

#set is an unordered collection of items (unique)

set1 = {1,2,3}

print(type(set1))

#string

string = "hi"

print(type(string))





